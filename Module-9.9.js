// 1. for loop: 
console.log('for loop: ');
for(let i = 0; i<=10; i++){
    console.log('the value of for loop : ', i);
}


// 2. while loop :
console.log('while loop: '); 
let n = 1;
while(n <= 10){
    console.log('the value of while loop :', n);
    n++;
}


// 3. do while :
console.log('do while loop: ');
let number = 1;
do{
    console.log('the value of do while loop:', number);
    number++;
}while(number<=10);