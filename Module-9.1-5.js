// print 1 - 4 :
for( let count = 1; count<5 ; count ++){
    console.log('print a count :',count);
}

// print 1 - 20 :

for(let number = 1; number <= 20 ; number ++){
    console.log('we are printing number:', number);
}

// print 20-10;

for(let lessNumber = 20; lessNumber >= 10 ; lessNumber --){
    console.log('less number is :', lessNumber);
}


// calculate 1-20 number in odd number use ing for loop: 

for(let isOdd = 1; isOdd <= 20 ; isOdd += 2){
    console.log('1-20 number in odd numbers calculate :', isOdd);
}


// calculate 1-20 number in even number use ing for loop: 

for(let isEven = 0; isEven <= 20; isEven += 2){
    console.log('1-20 number in even numbers calculate :', isEven);
}